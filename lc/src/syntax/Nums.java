package syntax;

public class Nums {
    public static void main(String[] args) {
        int m = 997/1000;
        float n = 988/1000;
        float p = 988/1000F;
        double x = 989/1000D;
        System.out.println(m);
        System.out.println(n);
        System.out.println(p);
        System.out.println(x);
    }
}
